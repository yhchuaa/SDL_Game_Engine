Map::Map(){
    ground = TextureManager::LoadTexture("../assets/map/Tileset_Ground.png");

    LoadMap(lvl1);

    src.x = 32;
    src.y = 0;
    src.h = 32;
    src.w = 32;

    des.h = src.h;
    des.w = src.w;
}

void Map::LoadMap(uint8_t arr[20][25]){
    for (int i = 0; i < 20; i++){
        for(int j = 0; j < 25; j++){
            map[i][j] = arr[i][j];
        }
    }
}

void Map::DrawMap(){
    uint8_t type = 0;
    for (int i = 0; i < 20; i++){
        for(int j = 0; j < 25; j++){
            type = map[i][j];

            des.x = j*32;
            des.y = i*32;
            switch (type){
            case 1:
                TextureManager::Draw(ground, src, des);
                break;
            default:
                break;
            }
        }
    }
}

